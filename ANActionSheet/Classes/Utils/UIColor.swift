internal extension UIColor {
    
    class func defaultBackgroundColor() -> UIColor {
        return whiteColor()
    }
    
    class func defaultTextColor() -> UIColor {
        return grayColor()
    }
    
    class func defaultButtonLabelColor() -> UIColor {
        return blackColor()
    }
    
    class func defaultButtonsBorderColor() -> UIColor {
        return clearColor()
    }
    
}
